public class App {
    public static void main(String[] args) {
        /*
         * tipodeDato identificador = dato;
         * tipodeDato identificador; --->> luego se inicializa
         * identificador= dato;
         */

        // tipos de datos enteros
        byte num1 = 1; // 1 byte
        short num2 = 2; // 2 byte
        int num3 = 3; // 4 byte
        long num4 = 4l; // 8 byte

        // punto flotante
        float num5 = 5.5f;
        double num6 = 6.12d;

        // caracter
        char caracter = 'a';

        // booleanos
        boolean verdadero = true;
        boolean falso = false;

        // cadena de texto
        String nombre = "Alejandro";
        String apellido = "Cano";

        // tipos de dato envoltorio
        Integer num7 = null;
        long num8 = 2L;
    }
}
